#include "shell.h"

int	ft_is_path(char *cmd)
{
	int	i;

	if (cmd == NULL)
		return (-1);
	i = 0;
	while (cmd[i])
	{
		if (cmd[i] == '/')
			return (1);
		i++;
	}
	return (0);
}
